    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String product = txtproduct.getText();
        String desc = txtdesc.getText();
        CategoryItem cat_item = (CategoryItem)txtcat.getSelectedItem();
        BrandItem brnd_item = (BrandItem)txtbrand.getSelectedItem();
        String cp = txtcp.getText();
        String rp = txtrp.getText();
        String qty = txtqty.getText();
        String barcode = txtbarcode.getText();        
        String status = txtstatus.getSelectedItem().toString();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/shoeinventorymanagementsystem","root","");
            pst = con1.prepareStatement("insert into product(Product, Description, Cat_ID, Brand_ID, Cost_Price, Retail_Price, Quantity, Barcode, Status)values(?, ?, ?, ?, ?, ?, ?, ?, ?)");
            pst.setString(1, product);
            pst.setString(2, desc);
            pst.setInt(3, cat_item.id);
            pst.setInt(4, brnd_item.id);
            pst.setString(5, cp);  
            pst.setString(6, rp);
            pst.setString(7, qty);
            pst.setString(8, barcode);
            pst.setString(9, status);         
              
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Product Successfully Saved");
            table_update();    
            
            
            txtproduct.setText("");
            txtdesc.setText("");
            txtcat.setSelectedIndex(-1);
            txtbrand.setSelectedIndex(-1); 
            txtcp.setText("");
            txtrp.setText("");  
            txtqty.setText("");
            txtbarcode.setText("");         
            txtstatus.setSelectedIndex(-1);
            txtproduct.requestFocus();
            
                    
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_addButtonActionPerformed

    private void CatTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CatTableMouseClicked
        // TODO add your handling code here:
        
        DefaultTableModel d1 = (DefaultTableModel)CatTable.getModel();
        int selectIndex = CatTable.getSelectedRow();
        
        txtproduct.setText(d1.getValueAt(selectIndex, 1).toString());
        txtdesc.setText(d1.getValueAt(selectIndex, 2).toString());
        //txtcat.setSelectedItem(d1.getValueAt(selectIndex, 3).toString());
        //txtbrand.setSelectedItem(d1.getValueAt(selectIndex, 4).toString());
        txtcp.setText(d1.getValueAt(selectIndex, 5).toString()); 
        txtrp.setText(d1.getValueAt(selectIndex, 6).toString());
        txtqty.setText(d1.getValueAt(selectIndex, 7).toString()); 
        txtbarcode.setText(d1.getValueAt(selectIndex, 8).toString());
        txtstatus.setSelectedItem(d1.getValueAt(selectIndex, 9).toString());
        
        
        String brand = d1.getValueAt(selectIndex, 4 ).toString();
        for(int j=0;j<=txtbrand.getItemCount();j++)
        {
            if(txtbrand.getItemAt(j).toString().equals(brand))
            {
                txtbrand.setSelectedIndex(j);
                break;
            }
        }
        
        String cat = d1.getValueAt(selectIndex, 3).toString();
        for(int j=0;j<=txtcat.getItemCount();j++)
        {
            if(txtcat.getItemAt(j).toString().equals(cat))
            {
                txtcat.setSelectedIndex(j);
                break;
            }
        }
        
        
        
        
    }//GEN-LAST:event_CatTableMouseClicked

