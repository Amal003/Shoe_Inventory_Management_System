    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel d1 = (DefaultTableModel)CatTable.getModel();
        int selectIndex = CatTable.getSelectedRow();
        
        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());
        
        int dialogResult = JOptionPane.showConfirmDialog(null, "Do You want to delete this product?","Warning..!!", JOptionPane.YES_NO_OPTION);
        
        if(dialogResult == JOptionPane.YES_OPTION)
        {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con1 = DriverManager.getConnection("jdbc:mysql://localhost/shoeinventorymanagementsystem","root","");
                pst = con1.prepareStatement("delete from product where Id = ?");
                pst.setInt(1, id);
                
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Product Successfully Deleted");
                table_update();            
                txtproduct.setText("");
                txtdesc.setText("");
                txtcat.setSelectedIndex(-1);
                txtbrand.setSelectedIndex(-1); 
                txtcp.setText("");
                txtrp.setText("");  
                txtqty.setText("");
                txtbarcode.setText("");         
                txtstatus.setSelectedIndex(-1);
                txtproduct.requestFocus();            
            } 
            catch (ClassNotFoundException ex)
            {
                Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
            } 
            catch (SQLException ex) 
            {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
    }//GEN-LAST:event_deleteButtonActionPerformed

